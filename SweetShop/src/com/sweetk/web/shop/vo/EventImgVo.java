package com.sweetk.web.shop.vo;

public class EventImgVo {

	int event_seq;
	int event_order;
	String img_path;
	String img_file_name;
	String img_org_name;

	public int getEvent_seq() {
		return event_seq;
	}
	public void setEvent_seq(int event_seq) {
		this.event_seq = event_seq;
	}
	public int getEvent_order() {
		return event_order;
	}
	public void setEvent_order(int event_order) {
		this.event_order = event_order;
	}
	public String getImg_path() {
		return img_path;
	}
	public void setImg_path(String img_path) {
		this.img_path = img_path;
	}
	public String getImg_file_name() {
		return img_file_name;
	}
	public void setImg_file_name(String img_file_name) {
		this.img_file_name = img_file_name;
	}
	public String getImg_org_name() {
		return img_org_name;
	}
	public void setImg_org_name(String img_org_name) {
		this.img_org_name = img_org_name;
	}

}
