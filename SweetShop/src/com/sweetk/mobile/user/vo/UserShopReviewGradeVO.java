package com.sweetk.mobile.user.vo;

public class UserShopReviewGradeVO {
	String code_id ;
	String code_name ;
	String reviewGrade ;
	String gradeCount;
	
	public String getCode_id() {
		return code_id;
	}
	public void setCode_id(String code_id) {
		this.code_id = code_id;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public String getReviewGrade() {
		return reviewGrade;
	}
	public void setReviewGrade(String reviewGrade) {
		this.reviewGrade = reviewGrade;
	}
	public String getGradeCount() {
		return gradeCount;
	}
	public void setGradeCount(String gradeCount) {
		this.gradeCount = gradeCount;
	}
	
}
